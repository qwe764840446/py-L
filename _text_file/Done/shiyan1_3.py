I = float(input("请输入当月利润，单位为万元："))
bns = 0  #初始化奖金总数
cat = [100, 60, 40, 20, 10, 0]  #金额分6档
pct = [0.01, 0.015, 0.03, 0.05, 0.075, 0.1]  #每档的提成比例
for i in range(6):
    if I > cat[i]:
        I = I - cat[i]  #注意：刨掉档位金额后，应将值重新赋给I，每一档位分开计算
        bns = bns + I * pct[i]  #每一档位的奖金累计相加
print('当月应发放奖金总数为%s万元' % bns)